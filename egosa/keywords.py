def count_hits(text: str, keywords: list[str] | None = None) -> dict[str, int]:
    """テキスト中のネガティブワード出現数を集計する。

    Args:
        text: 対象テキスト（記事タイトル＋要約など）。
        keywords: 使用するワード辞書。未指定なら既定の NEGATIVE_KEYWORDS。

    Returns:
        {ワード: 出現回数} の辞書（出現したワードのみ）。
    """
    if keywords is None:
        keywords = NEGATIVE_KEYWORDS
    if not text:
        return {}
    hits: dict[str, int] = {}
    for kw in keywords:
        n = text.count(kw)
        if n:
            hits[kw] = n
    return hits
